def format_date(date_str: str) -> str:
    # 自动判断是否需要格式化
    clean = date_str.strip()
    if '-' in clean:
        # 已经是标准格式，直接返回
        return clean
    elif len(clean) == 8 and clean.isdigit():
        # 纯数字格式，格式化
        return f"{clean[:4]}-{clean[4:6]}-{clean[6:]}"
    else:
        raise ValueError(f"无法识别的日期格式：{date_str}")

def format_dates_bulk(dates: str) -> list:
    return [format_date(line.strip()) for line in dates.strip().splitlines() if line.strip()]

def compare_date_sets(a: str, b: str) -> tuple:
    # 同样格式化两个字符串中的所有日期
    formatted_a = set(format_dates_bulk(a))
    formatted_b = set(format_dates_bulk(b))

    only_in_b = sorted(formatted_b - formatted_a)
    only_in_a = sorted(formatted_a - formatted_b)

    return only_in_b, only_in_a

